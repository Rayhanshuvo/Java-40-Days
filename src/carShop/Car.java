package carShop;

public class Car {

	private String nameOfCAr;
	private String modelOfCar;
	private String colorOfCar;
	private String companyOfCar;

	public String getNameOfCAr() {
		return nameOfCAr;
	}

	public void setNameOfCAr(String nameOfCAr) {
		this.nameOfCAr = nameOfCAr;
	}

	public String getModelOfCar() {
		return modelOfCar;
	}

	public void setModelOfCar(String modelOfCar) {
		this.modelOfCar = modelOfCar;
	}

	public String getColorOfCar() {
		return colorOfCar;
	}

	public void setColorOfCar(String colorOfCar) {
		this.colorOfCar = colorOfCar;
	}

	public String getCompanyOfCar() {
		return companyOfCar;
	}

	public void setCompanyOfCar(String companyOfCar) {
		this.companyOfCar = companyOfCar;
	}

}
